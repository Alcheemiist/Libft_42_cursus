#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*cpy;
	int		i;
	int		size;

	size = 0;
	while (src[size])
		size++;
	if (!(cpy = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
